let isGreater = 4 > 1;
console.log(isGreater);
// この型は通常 yes/no の値を格納するために使われます:
//  true は “yes” を意味し、 false は “no, 正しくない” を意味します。

const a = 3;
const b = 5;
if (a > b) {
  console.log("a is bigger");
}
// ifはもし〜ならの意味でif(a>b)がtrue(yes)ならifの部分が実行される
else {
  console.log("b is bigger");
}
// elesはそれ以外の意味で(a>b)がfalseならelesの部分が実行される

